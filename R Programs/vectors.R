# Creating a numeric vector
numbers <- c(10, 20, 30, 40)

# Creating a character vector
names <- c("Alice", "Bob", "Charlie")

# Creating a logical vector
logical_vals <- c(TRUE, FALSE, TRUE)

print(numbers)
print(names)
print(logical_vals)

# type conversion
vec <- c(10, "Hello", TRUE)
print(vec)
print(class(vec))

a <- 25
num <- as.integer(a)
print(num)
